package com.Arraysfor2;

public class FloatArray {

	public static void main(String[] args) {
		float[] height = new float[5];
		height[0] = 67.78f;
		height[1] = 55.87f;
		height[2] = 87.89f;
		height[3] = 78.88f;
		height[4] = 76.78f;
		
		for(int i=0;i<height.length;i++) {
			System.out.println(height[i]);
		}

	}

}
